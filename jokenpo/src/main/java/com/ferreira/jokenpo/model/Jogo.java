package com.ferreira.jokenpo.model;

public class Jogo {

    String codigoJogador;
    String nomeJogada;

    public String getCodigoJogador() {
        return this.codigoJogador;
    }

    public void setCodigoJogador(String codigoJogador) {
        this.codigoJogador = codigoJogador;
    }

    public String getNomeJogada() {
        return this.nomeJogada;
    }

    public void setNomeJogada(String nomeJogada) {
        this.nomeJogada = nomeJogada;
    }

}
